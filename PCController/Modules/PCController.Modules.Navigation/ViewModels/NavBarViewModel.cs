using PCController.Core.Mvvm;
using PCController.Services.Interfaces;
using Prism.Regions;

namespace PCController.Modules.Navigation.ViewModels
{
    public class NavBarViewModel : RegionViewModelBase
    {
        private string _message;
        public string Message
        {
            get { return _message; }
            set { SetProperty(ref _message, value); }
        }

        public NavBarViewModel(IRegionManager regionManager, IMessageService messageService) :
            base(regionManager)
        {
            Message = messageService.GetMessage();
        }

        public override void OnNavigatedTo(NavigationContext navigationContext)
        {
            //do something
        }
    }
}
